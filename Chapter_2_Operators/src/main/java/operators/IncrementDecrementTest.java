package operators;

public class IncrementDecrementTest {

	public static void main(String args[]) {
	
		int number = 0;
		
		System.out.println(number); //0
		System.out.println(++number); //1
		System.out.println(number); //1
		System.out.println(number--); //1
		System.out.println(number); //0
	}

}